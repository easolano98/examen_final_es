package com.example.demo.repository.modelo;

public class ContadorAsientos {
	
	private Integer asientosTotales;
	
	private Integer asientosOcupados;
	
	private Integer asientosDisponibles;

	//SET y GET
	public Integer getAsientosTotales() {
		return asientosTotales;
	}

	public void setAsientosTotales(Integer asientosTotales) {
		this.asientosTotales = asientosTotales;
	}

	public Integer getAsientosOcupados() {
		return asientosOcupados;
	}

	public void setAsientosOcupados(Integer asientosOcupados) {
		this.asientosOcupados = asientosOcupados;
	}

	public Integer getAsientosDisponibles() {
		return asientosDisponibles;
	}

	public void setAsientosDisponibles(Integer asientosDisponibles) {
		this.asientosDisponibles = asientosDisponibles;
	}

	
	
	
	
	
	
}
